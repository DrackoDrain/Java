package Java_Programs;
// Assignment Operator
public class program6 {
    public static void main(String args[]){
        int income = 40000;
        int bonus = 10000;

      //  income = income + bonus;
        income += bonus;
        System.out.println(income);

        int number = 10;
        int x = 2;

      //  number = number / x;
        number /= x;
        System.out.println(number);
    }

}
